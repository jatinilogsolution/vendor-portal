// ── VENDOR INVOICE PDF ─────────────────────────────────────────

export interface InvoicePdfData {
    invoiceNumber: string
    status: string
    vendorName: string
    vendorGst?: string
    vendorAddress?: string
    billTo?: string | null
    billToGstin?: string | null
    billToAddress?: string | null
    poNumber?: string | null
    piNumber?: string | null
    createdAt: Date
    submittedAt?: Date | null
    notes?: string | null
    subtotal: number
    discountAmount: number
    taxRate: number
    taxAmount: number
    totalAmount: number
    items: {
        description: string
        qty: number
        unitPrice: number
        tax: number
        total: number
    }[]
    payments?: {
        amount: number
        paymentMode: string | null
        transactionRef: string | null
        paymentDate: Date | null
        status: string
    }[]
}

export function generateInvoicePdf(data: InvoicePdfData): jsPDF {
    const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" })

    // Vendor is the issuer here
    header(
        doc,
        "TAX INVOICE",
        data.invoiceNumber,
        data.vendorName,
        data.vendorGst,
        data.vendorAddress,
    )

    const afterMeta = metaBlock(
        doc,
        [
            ["Bill To", data.billTo ?? "AWL India Pvt. Ltd."],
            ["GSTIN", data.billToGstin ?? "—"],
            ["Address", data.billToAddress ?? "—"],
            ["PO Ref.", data.poNumber ?? "—"],
            ["PI Ref.", data.piNumber ?? "—"],
        ],
        [
            ["Invoice No.", data.invoiceNumber],
            ["Date", new Date(data.createdAt).toLocaleDateString("en-IN")],
            ["Submitted", data.submittedAt
                ? new Date(data.submittedAt).toLocaleDateString("en-IN")
                : "—"],
            ["Status", data.status],
        ],
    )

    autoTable(doc, {
        startY: afterMeta + 2,
        head: [["#", "Description", "Qty", "Unit Price", "Tax %", "Total"]],
        body: data.items.map((item, i) => [
            i + 1,
            item.description,
            item.qty,
            INR(item.unitPrice),
            item.tax + "%",
            INR(item.total),
        ]),
        headStyles: { fillColor: [30, 41, 59], textColor: 255, fontSize: 9 },
        bodyStyles: { fontSize: 9 },
        columnStyles: {
            0: { cellWidth: 10, halign: "center" },
            1: { cellWidth: 75 },
            2: { cellWidth: 15, halign: "center" },
            3: { cellWidth: 30, halign: "right" },
            4: { cellWidth: 15, halign: "center" },
            5: { cellWidth: 35, halign: "right" },
        },
        theme: "striped",
    })

    let y = (doc as any).lastAutoTable.finalY + 4
    y = totalsBlock(doc, data.subtotal, data.discountAmount, data.taxRate, data.taxAmount, data.totalAmount, y)

    // Payment history if any
    if (data.payments && data.payments.length > 0) {
        y += 8
        doc.setFontSize(10).setFont("helvetica", "bold").text("Payment History", 14, y)
        y += 2
        autoTable(doc, {
            startY: y,
            head: [["Amount", "Mode", "Ref No.", "Date", "Status"]],
            body: data.payments.map((p) => [
                INR(p.amount),
                p.paymentMode ?? "—",
                p.transactionRef ?? "—",
                p.paymentDate ? new Date(p.paymentDate).toLocaleDateString("en-IN") : "—",
                p.status,
            ]),
            headStyles: { fillColor: [16, 185, 129], textColor: 255, fontSize: 8 },
            bodyStyles: { fontSize: 8 },
            theme: "grid",
        })
    }

    footer(doc, data.notes)
    return doc
}
